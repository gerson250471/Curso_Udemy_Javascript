function operacao(valor1, valor2){
    let sm=valor1+valor2;
    let sb=valor1-valor2;
    let mt=valor1*valor2;
    let dv=valor1/valor2;
    console.log(`${valor1} + ${valor2} = ${sm}`)
    console.log(`${valor1} - ${valor2} = ${sb}`)
    console.log(`${valor1} * ${valor2} = ${mt}`)
    console.log(`${valor1} / ${valor2} = ${dv}`)
}

operacao(4,5)